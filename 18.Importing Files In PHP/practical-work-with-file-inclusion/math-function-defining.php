
<?php
    function addition_operation($a, $b){
        return $a + $b;
    }

    function subtraction_operation($a, $b){
        return $a - $b;
    }

    function multiplication_operation($a, $b){
        return $a * $b;
    }

    function division_operation($a, $b){
        return $a / $b;
    }

    function power_operation($a, $b){
        return pow($a, $b);
    }
?>